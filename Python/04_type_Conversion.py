# File: 04_type_Conversion.py
# This file demonstrates type conversion in Python.
# Type conversion allows changing the data type of a variable to another type.
# Common conversions include int(), float(), str(), etc.
# This is useful for performing operations that require specific data types.

# Example 1: Converting to integer (int)
Age = 25  # Age is assigned as an integer
print(int(Age) + 3)  # Converts Age to int (already int) and adds 3, prints 28

# Example 2: Converting to float
price = 12.32  # price is assigned as a float
print(float(price) * 3)  # Converts price to float (already float) and multiplies by 3, prints 36.96
print(float(price) - 6)  # Converts price to float and subtracts 6, prints 6.32

