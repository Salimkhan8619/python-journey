# Program to demonstrate various data types in Python

A = 10  # Integer variable
Name = "salim khan"  # String variable for name
City = "Jaipur"  # String variable for city
price = 15.5  # Float variable for price

is_admin = True  # Boolean variable indicating admin status
is_banned = False  # Boolean variable indicating banned status

print(type(Name))  # Print the type of Name (str)
print(type(City))  # Print the type of City (str)
print(type(is_admin))  # Print the type of is_admin (bool)
print(type(is_banned))  # Print the type of is_banned (bool)
print(type(A))  # Print the type of A (int)
print(type(price))  # Print the type of price (float)
