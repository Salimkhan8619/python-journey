name = "Salim"  # Define a string variable

print(name[0:3])  # Slice from index 0 to 3 (exclusive), prints 'Sal'
print(name[1:3])  # Slice from index 1 to 3 (exclusive), prints 'al'
print(name[0:])   # Slice from index 0 to end, prints 'Salim'
print(name[1:-2]) # Slice from index 1 to -2 (second last), prints 'al' (since -2 is 'm', exclusive)