# File: 05_Input.py
# This file demonstrates how to take user input in Python using the input() function.
# The input() function reads a line from the user input (usually the keyboard) and returns it as a string.
# It's useful for interactive programs where you need data from the user.

# Example 1: Taking name input
Name = input("What is your Good Name:")  # Prompts the user to enter their name
print("Hello " + Name)  # Prints a greeting using the entered name

# Example 2: Taking age input
Age = input("Tell me your Age:")  # Prompts the user to enter their age
print(Age)  # Prints the entered age

# Example 3: Taking city input
City = (input("Where are you from:"))  # Prompts the user to enter their city
print("I like this Place: " + City)  # Prints a message with the entered city
